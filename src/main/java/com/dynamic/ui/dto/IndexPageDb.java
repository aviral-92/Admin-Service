package com.dynamic.ui.dto;

public class IndexPageDb {

	private int id;
	private String whatWeAre;
	private String whoWeAre;
	private String whatWeDo;
	private boolean status;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getWhatWeAre() {
		return whatWeAre;
	}

	public void setWhatWeAre(String whatWeAre) {
		this.whatWeAre = whatWeAre;
	}

	public String getWhoWeAre() {
		return whoWeAre;
	}

	public void setWhoWeAre(String whoWeAre) {
		this.whoWeAre = whoWeAre;
	}

	public String getWhatWeDo() {
		return whatWeDo;
	}

	public void setWhatWeDo(String whatWeDo) {
		this.whatWeDo = whatWeDo;
	}

	public boolean isStatus() {
		return status;
	}

	public void setStatus(boolean status) {
		this.status = status;
	}

}
